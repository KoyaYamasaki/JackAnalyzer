import Foundation

enum Symbol: String {
    case LBLACE = "{"
    case RBLACE = "}"
    case LPARENTHESIS = "("
    case RPARENTHESIS = ")"
    case LBLACKET = "["
    case RBLACKET = "]"
    case DOT = "."
    case COMMA = ","
    case SEMICOLON = ";"
    case PLUS = "+"
    case MINUS = "-"
    case ASTERISK = "*"
    case SLASH = "/"
    case AMPERSAND = "&"
    case PIPE = "|"
    case LANGLE = "<"
    case RANGLE = ">"
    case EQUAL = "="
    case TILDE = "~"
    case NONE = ""
}
